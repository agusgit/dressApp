package com.dresapp.DressApp.model.users;

public class BodyDescription {

    private double height;
    private double weight;

    private double hipWidth;
    private double legsLenght;

    private double shoulderWidth;
    private double feetSize;

    public BodyDescription() {
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public double getHipWidth() {
        return hipWidth;
    }

    public void setHipWidth(double hipWidth) {
        this.hipWidth = hipWidth;
    }

    public double getLegsLenght() {
        return legsLenght;
    }

    public void setLegsLenght(double legsLenght) {
        this.legsLenght = legsLenght;
    }

    public double getShoulderWidth() {
        return shoulderWidth;
    }

    public void setShoulderWidth(double shoulderWidth) {
        this.shoulderWidth = shoulderWidth;
    }

    public double getFeetSize() {
        return feetSize;
    }

    public void setFeetSize(double feetSize) {
        this.feetSize = feetSize;
    }
}
